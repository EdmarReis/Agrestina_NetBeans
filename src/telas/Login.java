    public void logar() {
		String sql = "select * from login where nome=? and senha=?";
		
		try {
			pst = conexao.prepareStatement(sql);
			pst.setString(1, txtUsuario.getText());
			pst.setString(2, txtSenha.getText());
			rs = pst.executeQuery();
			
			if(rs.next()) {
				Cadastro principal = new Cadastro();
				principal.setVisible(true);
				principal.setResizable(false);
				principal.setLocationRelativeTo(null);
                                Usuario usuario = new Usuario();
                                usuario.setNome(txtUsuario.getText());
                                principal.exportarNome(usuario);
                                Logger.getLogger(Cadastro.class.getName()).log(Level.WARNING, "Usu치rio "+txtUsuario.getText()+" logado", "");
                                this.dispose();
				conexao.close();
			}else {
				JOptionPane.showMessageDialog(null, "Usu치rio e/ou senha inv치lido(s)");
                                Logger.getLogger(Cadastro.class.getName()).log(Level.SEVERE, "Tentativa de login, usuario "+txtUsuario.getText(), "");
			}

		} catch (Exception e) {
			// TODO: handle exception
			JOptionPane.showMessageDialog(null, e);
		}
	}

